const fs = require('fs');
const path = require('path');

const homepageDir = path.resolve(__dirname, '../custom/homepages/v2/homepages/v2');
const memeDir = path.resolve(__dirname, '../custom/homepages/v2/homepages/meme');
const dirs = [homepageDir, memeDir].filter((dir) => fs.existsSync(dir));

if (dirs.length === 0) process.exit(0);

dirs.forEach((dir) => {
  fs.readdirSync(dir).forEach((file) => {
    if (file.endsWith('.js')) {
      const oldPath = path.join(dir, file);
      const newPath = path.join(dir, file.replace(/\.js$/, '.cjs'));
      fs.copyFileSync(oldPath, newPath);
      console.log(`Copied ${path.relative(process.cwd(), oldPath)} -> ${path.basename(newPath)}`);
    }
  });
});

dirs.forEach((dir) => {
  fs.readdirSync(dir).forEach((file) => {
    if (file.endsWith('.cjs')) {
      const filePath = path.join(dir, file);
      const content = fs.readFileSync(filePath, 'utf8');
      const fixed = content.replace(/require\((['"])(.+?)\.js\1\)/g, 'require($1$2.cjs$1)');
      if (fixed !== content) {
        fs.writeFileSync(filePath, fixed);
        console.log(`Fixed require paths in ${path.relative(process.cwd(), filePath)}`);
      }
    }
  });
});

// Create a browser-safe ESM entry when the homepage repo only provides index.cjs.
const indexCjsPath = path.join(homepageDir, 'index.cjs');
const indexTsPath = path.join(homepageDir, 'index.ts');
if (fs.existsSync(indexCjsPath)) {
  const cjsEntry = fs.readFileSync(indexCjsPath, 'utf8');
  const requireMatches = [...cjsEntry.matchAll(/^\s*const\s+([A-Za-z0-9_$]+)\s*=\s*require\((['"])(.+?)\2\);/gm)];
  const requiresByName = new Map(requireMatches.map((match) => [match[1], match[3]]));
  const exportObject = cjsEntry.match(/module\.exports\s*=\s*\{([\s\S]*?)\};/);
  const exportMatches = exportObject
    ? [...exportObject[1].matchAll(/^\s*(['"]?[A-Za-z0-9_$-]+['"]?)\s*:\s*([A-Za-z0-9_$]+),?/gm)]
    : [];

  if (requireMatches.length > 0 && exportMatches.length > 0) {
    const imports = requireMatches.map((match) => `import ${match[1]} from '${match[3]}';`).join('\n');
    const exportLines = exportMatches
      .filter((match) => requiresByName.has(match[2]))
      .map((match) => `  ${match[1]}: ${match[2]},`)
      .join('\n');
    const namedExports = exportMatches
      .filter((match) => /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(match[1]) && requiresByName.has(match[2]))
      .map((match) => `export const ${match[1]} = ${match[2]};`)
      .join('\n');
    const shim = `// Auto-generated by scripts/fix-homepage-extensions.cjs
${imports}

${namedExports}

export default {
${exportLines}
};
`;

    fs.writeFileSync(indexTsPath, shim);
    console.log(`Created custom homepage ESM shim: ${path.relative(process.cwd(), indexTsPath)}`);
  }
}
